int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	result;
	int	pm_num;
	
	i = 0;
	sign = 1;
	pm_num = 0;
	result = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	while (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -sign;
		i++;
		pm_num++;
	}
	if (pm_num > 1)
		return (0);
	while (str[i] >= '0' && str[i] <= '9')
	{
		result = result * 10 + str[i] - '0';
		i++;
	}
	return (result * sign);
}
